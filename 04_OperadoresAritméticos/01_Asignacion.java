public class Asignacion {
    public static void main(String[] args) {
        // Asignación de valores a variables
        int x = 10;  // Asigna 10 a la variable x
        double y = 3.14;  // Asigna 3.14 a la variable y
        String nombre = "Juan";  // Asigna "Juan" a la variable nombre

        // Mostrar los valores asignados
        System.out.println("Valor de x: " + x);
        System.out.println("Valor de y: " + y);
        System.out.println("Nombre: " + nombre);

        // Uso de operadores de asignación compuesta
        x += 5;  // x = x + 5
        y *= 2;  // y = y * 2
        System.out.println("Nuevo valor de x: " + x);  // 15
        System.out.println("Nuevo valor de y: " + y);  // 6.28
    }
}
