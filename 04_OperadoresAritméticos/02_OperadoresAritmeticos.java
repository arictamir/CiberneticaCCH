public class OperadoresAritmeticos {
    public static void main(String[] args) {
        int a = 10, b = 4;

        // Suma
        System.out.println("Suma: " + (a + b));  // 14

        // Resta
        System.out.println("Resta: " + (a - b));  // 6

        // Multiplicación
        System.out.println("Multiplicación: " + (a * b));  // 40

        // División (resultado entero)
        System.out.println("División: " + (a / b));  // 2

        // Módulo (residuo)
        System.out.println("Módulo: " + (a % b));  // 2
    }
}
